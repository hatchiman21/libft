#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long int	m;
	char		r;

	m = n;
	if (m < 0)
	{
		write (fd, "-", 1);
		m *= -1;
	}
	if (m > 9)
		ft_putnbr_fd(m / 10, fd);
	r = (m % 10) + '0';
	write (fd, &r, 1);
}
